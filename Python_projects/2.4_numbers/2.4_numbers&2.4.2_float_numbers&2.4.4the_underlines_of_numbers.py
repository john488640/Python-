#Python中的数字类型有四种：整数、浮点数、复数、布尔值。
#整数类型：整数类型是最基本的数字类型，它表示整数值。整数类型可以是正数、负数、0。
#浮点数类型：浮点数类型是小数点后带有小数位的数字类型。浮点数类型可以表示小数。
#复数类型：复数类型是由实数部分和虚数部分构成的数字类型。复数类型可以表示复数。
#布尔值类型：布尔值类型只有两个值：True和False。布尔值类型可以表示真或假。


